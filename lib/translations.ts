export function getTranslation(language: Language | undefined, key: string): string {
  // Always use a safe language code
  const safeLang: Language = language && translations[language] ? language : "en"

  const keys = key.split(".")
  let value: any = translations[safeLang]

  for (const k of keys) {
    value = value?.[k]
  }

  return value || translations.en[key as keyof typeof translations.en] || key
}
